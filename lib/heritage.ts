// lib/heritage.ts
// --------------------------------------------------------
// HERITAGE セクション用のデータモデルとスタティックデータ
// - 時代ごとの「ERA」
// - ブランドごとの「BRAND STRIPE」
// をまとめて管理します。
// --------------------------------------------------------

export type HeritageEra = {
  kind: "ERA";
  slug: string;
  /** 1970s / 1990s のような大まかな年代表記 */
  year: string;
  /** 「〜1980年代前半」など、ざっくりした期間表示（任意） */
  periodJa?: string;
  /** 時代ブロックのタイトル（一覧・詳細の両方で使用） */
  title: string;
  /** 時代の概要。HERITAGE 一覧の説明文にも使用 */
  description: string;
  /** もう一段階踏み込んだ補足（一覧で小さめに表示） */
  subDescription?: string;
  /** ERA をラベル付けするタグ群（UI のチップ表示用） */
  tags?: string[];
  /** 代表的なブランドなど。ERA の右側に小さく表示 */
  accentBrand?: string;
  /** 詳細ページ用の本文（空行区切りで複数段落に分割されます） */
  body?: string;
};

export type HeritageBrandStripe = {
  kind: "BRAND";
  slug: string;
  /** ブランド名（BMW / Mercedes-Benz など） */
  brand: string;
  /** ブランドキャラクターを一言で表すタグライン */
  tagline: string;
  /** ブランドストライプの概要テキスト（一覧・詳細の両方で使用） */
  summary: string;
  /** 「〜2000年代前半」など、フォーカスする年代帯（任意） */
  focusYears?: string;
  /** 補足メモ。CARS や COLUMN との付き合い方などを記述 */
  note?: string;
  /** 詳細ページ用の本文（空行区切り） */
  body?: string;
};

/**
 * HERITAGE で扱うノードの総称。
 * /heritage/[slug] では ERA / BRAND をこの型で共通処理します。
 */
export type HeritageNode = HeritageEra | HeritageBrandStripe;

