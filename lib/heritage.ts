// lib/heritage.ts
// --------------------------------------------------------
// HERITAGE セクション用のデータモデルとスタティックデータ
// - ERA: 時代ごとのストーリー
// - BRAND: ブランドごとのキャラクター変遷
// - CAR: 世代ごとの車種ストーリー（前後世代への回遊用）
// --------------------------------------------------------

export type HeritageEra = {
  kind: "ERA";
  slug: string;
  year: string; // "1970s" など
  periodJa?: string; // "〜1980年代前半" など
  title: string;
  description: string;
  subDescription?: string;
  tags?: string[];
  accentBrand?: string;
  /** 記事本文（空行で段落分割されます） */
  body?: string;
  /** 冒頭にまとめる「ポイント」の箇条書き */
  highlights?: string[];
};

export type HeritageBrandStripe = {
  kind: "BRAND";
  slug: string;
  brand: string;
  tagline: string;
  summary: string;
  focusYears?: string;
  note?: string;
  body?: string;
  highlights?: string[];
  /** ブランド内のざっくり年表 */
  timeline?: { year: string; text: string }[];
};

export type HeritageCarStory = {
  kind: "CAR";
  slug: string;
  /** CARS データの slug （存在しなくても落ちないように扱う） */
  carSlug: string;
  /** BMW 3 Series E46 などの記事タイトル */
  title: string;
  /** 「E46 / 4th generation」などの世代ラベル */
  generationLabel?: string;
  /** 1998–2005 などの年式レンジ */
  years?: string;
  /** メーカー名（表示用） */
  maker?: string;
  /** モデル系統名（3 Series など） */
  modelLine?: string;
  /** 関連ブランドストーリーへの紐付け（存在すればリンク表示） */
  brandSlug?: string;
  /** この記事の要約テキスト */
  summary: string;
  body?: string;
  highlights?: string[];
  /** 前後世代の記事 slug（存在すれば prev / next ナビに出す） */
  previousSlug?: string;
  nextSlug?: string;
};

export type HeritageNode =
  | HeritageEra
  | HeritageBrandStripe
  | HeritageCarStory;

