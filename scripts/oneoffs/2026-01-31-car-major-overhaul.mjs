function ensureArray(v) {
  if (Array.isArray(v)) return v;
  if (typeof v === "string") {
    const s = v.trim();
    if (!s) return [];
    // If someone accidentally stored a paragraph, keep it as a single bullet.
    return [s];
  }
  return [];
}

